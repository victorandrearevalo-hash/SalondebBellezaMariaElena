    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // 1Ô∏è‚É£ Capturar datos del formulario
        String nombre = txtNombres.getText().trim();
        String apellido = txtApellidos.getText().trim();
        String correo = txtEmail.getText().trim();
        String telefono = txtTelefono.getText().trim();

        // üîπ Obtener distrito seleccionado
        Distrito distritoSeleccionado = (Distrito) cboDistrito.getSelectedItem();
        int idDistrito = distritoSeleccionado != null ? distritoSeleccionado.getIdDistrito() : 0;
        boolean estado = chkEstado.isSelected();

        // üîπ Validar campos obligatorios
        if (nombre.isEmpty() || apellido.isEmpty() || correo.isEmpty()
                || telefono.isEmpty() || idDistrito == 0) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Todos los campos son obligatorios.");
            return;
        }

        // üîπ Validar correo
        if (!correo.matches("^[\\w.-]+@[\\w.-]+\\.[A-Za-z]{2,6}$")) {
            JOptionPane.showMessageDialog(this,
                    "‚ö†Ô∏è El correo ingresado no tiene un formato v√°lido. Debe contener '@' y un dominio.",
                    "Validaci√≥n",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // üîπ Validar tel√©fono EXACTAMENTE 9 d√≠gitos y que no inicie con 0
        if (!telefono.matches("^[1-9][0-9]{8}$")) {
            JOptionPane.showMessageDialog(this,
                    "‚ö†Ô∏è El tel√©fono debe tener 9 d√≠gitos y no empezar con 0.",
                    "Validaci√≥n",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        // 3Ô∏è‚É£ Crear objeto Cliente
        Cliente c = new Cliente(0, "----Seleccione Cliente----");
        c.setNombre(nombre);
        c.setApellido(apellido);
        c.setCorreo(correo);
        c.setTelefono(telefono);
        c.setDistrito(String.valueOf(idDistrito)); // si tu clase usa String
        c.setEstado(chkEstado.isSelected());

        // 4Ô∏è‚É£ Llamar al controlador
        clienteController controlador = new clienteController();
        boolean exito;
        // 5Ô∏è‚É£ Verificar resultado
        if (modoEdicion) {
            // Modo edici√≥n
            c.setId(idClienteEditar);
            exito = controlador.actualizarCliente(c);
        } else {
            // Modo registro
            exito = controlador.registrarCliente(c);
        }

        // 4Ô∏è‚É£ Obtener mensaje y tipo desde el controlador
        String msj = controlador.getMensaje();
        int tipo = controlador.getTipoMensaje();

        // 5Ô∏è‚É£ Mostrar resultado seg√∫n tipo
        switch (tipo) {
            case 0: // ERROR
                JOptionPane.showMessageDialog(this, msj, "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case 1: // ADVERTENCIA
                JOptionPane.showMessageDialog(this, msj, "Advertencia", JOptionPane.WARNING_MESSAGE);
                break;
            case 2: // √âXITO
                JOptionPane.showMessageDialog(this, msj, "√âxito", JOptionPane.INFORMATION_MESSAGE);
                if (!modoEdicion) {
                    Limpiar(); // Limpiar solo registro
                }
                if (modoEdicion) {
                    dispose();  // Cerrar ventana edici√≥n
                }
                break;

            default:
                JOptionPane.showMessageDialog(this, "Tipo de mensaje desconocido: " + msj, "Error", JOptionPane.ERROR_MESSAGE);
                break;
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

