    private void cargarDistrito() {
        distritoController controller = new distritoController();
        List<Distrito> lista = controller.obtenerDistrito();

        cboDistrito.removeAllItems();
        cboDistrito.addItem(new Distrito(0, "----Seleccione un Distrito----"));
        for (Distrito a : lista) {
            cboDistrito.addItem(a);
        }
    }

    private void seleccionarDistrito(String valor) {
        if (valor == null || valor.isEmpty()) {
            return;
        }

        for (int i = 0; i < cboDistrito.getItemCount(); i++) {
            Distrito d = (Distrito) cboDistrito.getItemAt(i);

            // üî∏ Si es num√©rico, comparar por ID
            if (valor.matches("\\d+") && d.getIdDistrito() == Integer.parseInt(valor)) {
                cboDistrito.setSelectedIndex(i);
                return;
            }

            // üî∏ Si no es num√©rico, comparar por nombre
            if (d.getNombre().equalsIgnoreCase(valor)) {
                cboDistrito.setSelectedIndex(i);
                return;
            }
        }
    }

