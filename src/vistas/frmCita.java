    private void cargarCitas() {
        try {
            citaController controlador = new citaController();
            ArrayList<Cita> lista = controlador.listarCitas();

            DefaultTableModel tb = (DefaultTableModel) tblCitas.getModel();
            tb.setRowCount(0);

            if (lista == null || lista.isEmpty()) {
                lblAviso.setText("‚ö†Ô∏è No hay citas registradas.");
                lblAviso.setForeground(Color.RED);
                lblAviso.setVisible(true);
                return;
            }

            int pendientes = 0;
            int proximas = 0;
            LocalDate hoy = LocalDate.now();

            DateTimeFormatter formatoHora = DateTimeFormatter.ofPattern("HH:mm");

            for (Cita c : lista) {
                LocalDate fechaCita = c.getFechaCita().toLocalDate(); // ‚úÖ Obtener fecha de la cita

                // Contar pendientes
                if ("Pendiente".equalsIgnoreCase(c.getEstadoCita())) {
                    pendientes++;
                }

                // Contar pr√≥ximas (por ejemplo, dentro de 3 d√≠as)
                if (fechaCita != null && !fechaCita.isBefore(hoy) && !fechaCita.isAfter(hoy.plusDays(3))) {
                    proximas++;
                }

                // Formatear hora "HH:mm"
                String horaFormateada = c.getHoraCita().toLocalTime().format(formatoHora);

                Object[] fila = {
                    c.getIdCita(), // 0
                    c.getFechaCita(), // 1
                    horaFormateada, // 2 -- Hora formateada
                    c.getNombreCliente(), // 3
                    c.getNombreTrabajador(), // 4
                    c.getIdArea(), // 5 (oculto)
                    c.getNombreArea(), // 6
                    c.getIdServicio(), // 7 (oculto)
                    c.getNombreServicio(), // 8
                    c.getEstadoCita(), // 9
                    c.getNombreUsuario(), // 10 (oculto)
                    c.getObservacionCita(),// 11 (oculto)
                    "Acciones" // 12
                };
                tb.addRow(fila);

            }

            // Configurar botones de acci√≥n
            tblCitas.getColumn("Acciones").setCellRenderer(new ButtonPanelCellCita(tblCitas));
            tblCitas.getColumn("Acciones").setCellEditor(new ButtonPanelCellCita(tblCitas));
            tblCitas.setRowHeight(25);

            // üîπ Mensaje din√°mico en lblAviso
            if (pendientes > 0) {
                lblAviso.setText("üìÖ Hay " + pendientes + " cita(s) pendiente(s). Rev√≠salas cerca a la fecha acordada.");
                lblAviso.setForeground(new Color(255, 140, 0)); // Naranja suave
            } else if (proximas > 0) {
                lblAviso.setText("‚è∞ Hay " + proximas + " cita(s) pr√≥xima(s) en los pr√≥ximos d√≠as. Revisa tu agenda.");
                lblAviso.setForeground(new Color(255, 165, 0)); // Naranja claro
            } else {
                lblAviso.setText("‚úÖ Se cargaron " + lista.size() + " citas registradas. No hay pendientes ni pr√≥ximas.");
                lblAviso.setForeground(new Color(0, 128, 0)); // Verde
            }

            lblAviso.setVisible(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "‚ùå Error al cargar citas: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnNuevaCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevaCitaActionPerformed
        // Obtener usuario logueado
        Usuario usr = SesionUsuario.getUsuarioActual();

        if (usr == null) {
            JOptionPane.showMessageDialog(null, "üö´ No hay usuario activo.", "Acceso denegado", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validar rol: Administrador Limitado no puede registrar clientes
        if ("Administrador Limitado".equalsIgnoreCase(usr.getRol())) {
            JOptionPane.showMessageDialog(null,
                    "üö´ No tienes permiso para Registrar Cita.",
                    "Acceso denegado", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JDesktopPane desktopPane = this.getDesktopPane();
        if (desktopPane == null) {
            return;
        }

        frmNuevaCita citaForm = null;
        for (JInternalFrame frame : desktopPane.getAllFrames()) {
            if (frame instanceof frmNuevaCita) {
                citaForm = (frmNuevaCita) frame;
                break;
            }
        }

        if (citaForm != null) {
            try {
                citaForm.setIcon(false);
                citaForm.setSelected(true);
                citaForm.toFront();
                citaForm.habilitarCampos(); // habilita modo registro
                citaForm.configurarEstadoPendiente(); // üîπ Aqu√≠ fijamos el estado
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            citaForm = new frmNuevaCita();
            desktopPane.add(citaForm);
            citaForm.setVisible(true);
            citaForm.habilitarCampos(); // ‚úÖ Habilitar los campos al abrir por primera vez
            citaForm.configurarEstadoPendiente(); // üîπ Fijar estado al abrir
        }
    }//GEN-LAST:event_btnNuevaCitaActionPerformed

