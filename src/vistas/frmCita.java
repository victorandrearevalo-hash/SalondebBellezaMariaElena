    private void btnBuscarCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCitaActionPerformed
        citaController ctrl = new citaController();
        String criterio = "";

        // üîπ Restaurar colores por defecto
        txtCliente.setBackground(Color.white);
        txtTrabajador.setBackground(Color.white);
        if (dtFecha.getDate() != null) {
            dtFecha.getDateEditor().getUiComponent().setBackground(Color.white);
        }

        // Construir el criterio seg√∫n lo que ingrese el usuario
        if (!txtCliente.getText().trim().isEmpty()) {
            criterio = txtCliente.getText().trim();
        } else if (!txtTrabajador.getText().trim().isEmpty()) {
            criterio = txtTrabajador.getText().trim();
        } else if (dtFecha.getDate() != null) { // si usas JDateChooser
            criterio = new java.sql.Date(dtFecha.getDate().getTime()).toString();
        }
        // üîπ Validar entrada    
        if (criterio.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese nombre, apellido o fecha para buscar");
            return;
        }

        ArrayList<Cita> lista = ctrl.buscarCita(criterio);

        DefaultTableModel tb = (DefaultTableModel) tblCitas.getModel();
        tb.setRowCount(0);

        DateTimeFormatter formatoHora = DateTimeFormatter.ofPattern("HH:mm");
        DateTimeFormatter formatoFecha = DateTimeFormatter.ofPattern("yyyy-MM-dd");

        if (lista != null && !lista.isEmpty()) {
            for (Cita c : lista) {
                String horaFormateada = c.getHoraCita().toLocalTime().format(formatoHora);
                String fechaFormateada = c.getFechaCita().toLocalDate().format(formatoFecha);

                Object[] fila = {
                    c.getIdCita(),
                    fechaFormateada,
                    horaFormateada,
                    c.getNombreCliente(),
                    c.getNombreTrabajador(),
                    c.getIdArea(), // 5 (oculto)
                    c.getNombreArea(), // 6
                    c.getIdServicio(), // 7 (oculto)
                    c.getNombreServicio(),
                    c.getEstadoCita(),
                    c.getNombreUsuario(), // 10 (oculto)
                    c.getObservacionCita(),// 11 (oculto)
                    "Acciones"
                };
                tb.addRow(fila);
            }
            // üîπ Resaltar en verde si encontr√≥ resultados
            if (!txtCliente.getText().trim().isEmpty()) {
                txtCliente.setBackground(new Color(144, 238, 144));
            }
            if (!txtTrabajador.getText().trim().isEmpty()) {
                txtTrabajador.setBackground(new Color(144, 238, 144));
            }
            if (dtFecha.getDate() != null) {
                dtFecha.getDateEditor().getUiComponent().setBackground(new Color(144, 238, 144));
            }

        } else {
            // üîπ Resaltar en rojo si no hay resultados
            if (!txtCliente.getText().trim().isEmpty()) {
                txtCliente.setBackground(new Color(255, 160, 122));
            }
            if (!txtTrabajador.getText().trim().isEmpty()) {
                txtTrabajador.setBackground(new Color(255, 160, 122));
            }
            if (dtFecha.getDate() != null) {
                dtFecha.getDateEditor().getUiComponent().setBackground(new Color(255, 160, 122));
            }
            JOptionPane.showMessageDialog(null, "‚ùå No se encontraron citas", "Error", JOptionPane.ERROR_MESSAGE);
        }
        // üßπüîπ LIMPIAR CAMPOS DE B√öSQUEDA DESPU√âS DE BUSCAR
        txtCliente.setText("");
        txtTrabajador.setText("");
        dtFecha.setDate(null);  // <- Esto limpia el JDateChooser
    }//GEN-LAST:event_btnBuscarCitaActionPerformed

