    private void alinearColumnas() {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        // Centramos columnas espec√≠ficas
        int[] columnasCentradas = {1, 2, 9, 12}; // Fecha, Hora, Estado, Acciones
        for (int col : columnasCentradas) {
            tblCitas.getColumnModel().getColumn(col).setCellRenderer(centerRenderer);
        }
    }

