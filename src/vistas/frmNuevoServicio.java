    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        // 1Ô∏è‚É£ Capturar datos del formulario
        String nombreServicio = txtServicio.getText().trim();
        String precioStr = txtPrecio.getText().trim();
        String duracionStr = txtDuracion.getText().trim();
        String nombreNuevaArea = txtNuevaArea.getText().trim();

        // √Årea seleccionada del combo
        Area areaSeleccionada = (Area) cboAreas.getSelectedItem();

        // Validar campos obligatorios
        if (nombreServicio.isEmpty() || precioStr.isEmpty() || duracionStr.isEmpty()
                || (areaSeleccionada == null && nombreNuevaArea.isEmpty())) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Todos los campos son obligatorios.");
            return;
        }
        double precio;
        int duracion;

        try {
            precio = Double.parseDouble(precioStr);
            duracion = Integer.parseInt(duracionStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Precio o duraci√≥n no son v√°lidos.");
            return;
        }

        // 2Ô∏è‚É£ Crear objeto Servicio
        Servicio s = new Servicio();
        s.setNombre(nombreServicio);
        s.setPrecio(precio);
        s.setDuracion(duracion);
        s.setArea(areaSeleccionada);  // atributo nuevo en DTO

        // 3Ô∏è‚É£ Llamar al controller
        servicioController sc = new servicioController();
        boolean exito;
        if (modoEdicion) {
            s.setIdServicio(idServicioEditar); // si est√° editando
            exito = sc.actualizar(s);
        } else {
            exito = sc.registrar(s);
        }

        // 5Ô∏è‚É£ Mostrar mensaje seg√∫n el DAO
        switch (sc.getTipoMensaje()) {
            case 0:
                JOptionPane.showMessageDialog(this, sc.getMensaje(), "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case 1:
                JOptionPane.showMessageDialog(this, sc.getMensaje(), "Advertencia", JOptionPane.WARNING_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(this, sc.getMensaje(), "√âxito", JOptionPane.INFORMATION_MESSAGE);
                Limpiar(); // limpiar campos
                actualizarComboAreas(); // actualizar combo si es nueva √°rea
                break;
            default:
                JOptionPane.showMessageDialog(this, "Resultado desconocido: " + sc.getMensaje());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    // üîπ Cargar datos del servicio seleccionado
    public void cargarDatosParaEditar(Servicio s) {
        if (s == null) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è No se han recibido datos para editar.");
            return;
        }

        // Cambia el formulario a modo EDICI√ìN
        this.modoEdicion = true;
        this.idServicioEditar = s.getIdServicio(); // <-- Guardar ID para actualizar
        this.setTitle("Editar Servicio");

        // Llenar campos
        txtNuevaArea.setText(String.valueOf(s.getArea().getIdArea()));

        // 1Ô∏è‚É£ Actualizar combo de √°reas
        actualizarComboAreas(); // Llenamos el combo con todas las √°reas

        // 2Ô∏è‚É£ Seleccionar el √°rea del servicio
        for (int i = 0; i < cboAreas.getItemCount(); i++) {
            Area a = (Area) cboAreas.getItemAt(i);
            if (a.getIdArea() == s.getArea().getIdArea()) {
                cboAreas.setSelectedIndex(i);
                break;
            }
        }

        // 1Ô∏è‚É£ Seleccionar √°rea en el combo seg√∫n el objeto Area del servicio
        cboAreas.setSelectedItem(s.getArea());

        txtServicio.setText(s.getNombre());
        txtDuracion.setText(String.valueOf(s.getDuracion()));
        txtPrecio.setText(String.valueOf(s.getPrecio()));

        // Permisos del usuario (si lo usas)
        //aplicarPermisosServicio();
        // Cambiar texto del bot√≥n principal
        btnRegistrar.setText("ACTUALIZAR");
    }

    private void btnVolverDashboardActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVolverDashboardActionPerformed
        // Obtener el desktopPane del formulario padre
        JDesktopPane desktopPane = this.getDesktopPane();
        if (desktopPane == null) {
            System.err.println("‚ö†Ô∏è No se pudo acceder al desktopPane del padre.");
            return;
        }

        frmDashboard dasbboardForm = null;

        // Recorremos los frames existentes
        for (JInternalFrame frame : desktopPane.getAllFrames()) {
            if (frame instanceof frmNuevoUsuario) { // verificar la clase correcta
                dasbboardForm = (frmDashboard) frame;
                break;
            }
        }

        // Si ya existe, traerlo al frente
        if (dasbboardForm != null) {
            try {
                dasbboardForm.setIcon(false); // si estaba minimizado
                dasbboardForm.setSelected(true);
                dasbboardForm.toFront();
            } catch (PropertyVetoException e) {
                e.printStackTrace();
            }
        } else {
            // Si no existe, crear nueva instancia y agregar al desktopPane
            dasbboardForm = new frmDashboard();
            desktopPane.add(dasbboardForm);
            dasbboardForm.setVisible(true);
        }
    }//GEN-LAST:event_btnVolverDashboardActionPerformed

