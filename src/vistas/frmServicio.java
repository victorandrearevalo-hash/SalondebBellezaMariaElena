    private void btnNuevoServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoServicioActionPerformed
        // Obtener usuario logueado
        Usuario usr = SesionUsuario.getUsuarioActual();

        if (usr == null) {
            JOptionPane.showMessageDialog(null, "üö´ No hay usuario activo.", "Acceso denegado", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validar rol: Administrador Limitado no puede registrar clientes
        if ("Administrador Limitado".equalsIgnoreCase(usr.getRol())) {
            JOptionPane.showMessageDialog(null,
                    "üö´ No tienes permiso para Registrar Cita.",
                    "Acceso denegado", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JDesktopPane desktopPane = this.getDesktopPane();
        if (desktopPane == null) {
            return;
        }

        frmNuevoServicio servicioForm = null;
        for (JInternalFrame frame : desktopPane.getAllFrames()) {
            if (frame instanceof frmNuevaCita) {
                servicioForm = (frmNuevoServicio) frame;
                break;
            }
        }

        if (servicioForm != null) {
            try {
                servicioForm.setIcon(false);
                servicioForm.setSelected(true);
                servicioForm.toFront();
                //servicioForm.habilitarCampos(); // habilita modo registro
                //servicioForm.configurarEstadoPendiente(); // üîπ Aqu√≠ fijamos el estado
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            servicioForm = new frmNuevoServicio();
            desktopPane.add(servicioForm);
            servicioForm.setVisible(true);
            //servicioForm.habilitarCampos(); // ‚úÖ Habilitar los campos al abrir por primera vez
            //servicioForm.configurarEstadoPendiente(); // üîπ Fijar estado al abrir
        }
    }//GEN-LAST:event_btnNuevoServicioActionPerformed

    private void btnBuscarServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarServicioActionPerformed
        servicioController ctrl = new servicioController();
        String criterio = "";

        // Resetear colores de campos
        txtCodigo.setBackground(Color.white);
        txtArea.setBackground(Color.white);
        txtServicio.setBackground(Color.white);

        // üîπ Prioridad: busca por c√≥digo o nombre
        if (!txtCodigo.getText().trim().isEmpty()) {
            criterio = txtCodigo.getText().trim();
        } else if (!txtArea.getText().trim().isEmpty()) {
            criterio = txtArea.getText().trim();
        } else if (!txtServicio.getText().trim().isEmpty()) {
            criterio = txtServicio.getText().trim();
        }
        if (criterio.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese c√≥digo o nombre para buscar", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        // Limpiar tabla
        DefaultTableModel tb = (DefaultTableModel) tblServicio.getModel();
        tb.setRowCount(0);

        // Buscar usando el controller
        ArrayList<Servicio> lista = ctrl.buscar(criterio);

        if (!lista.isEmpty()) {
            for (Servicio s : lista) {
                Object[] fila = {
                    s.getArea().getIdArea(),
                    s.getArea().getNombreArea(),
                    s.getIdServicio(),
                    s.getNombre(),
                    s.getPrecio(),
                    s.getDuracion(),
                    "Editar / Eliminar"
                };
                tb.addRow(fila);
            }

            JOptionPane.showMessageDialog(null, "‚úÖ " + lista.size() + " servicio(s) encontrado(s)", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Colorear en rojo si no encuentra
            txtCodigo.setBackground(Color.red);
            txtArea.setBackground(Color.red);
            txtServicio.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "‚ùå No se encontraron servicios o √°reas", "Error", JOptionPane.ERROR_MESSAGE);
        }
        Limpiar();
        
        txtCodigo.setBackground(Color.white);
        txtArea.setBackground(Color.white);
        txtServicio.setBackground(Color.white);
    }//GEN-LAST:event_btnBuscarServicioActionPerformed

