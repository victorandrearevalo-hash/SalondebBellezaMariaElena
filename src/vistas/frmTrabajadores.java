    private void btnNuevoTrabajadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoTrabajadorActionPerformed
        // Obtener el desktopPane del formulario actual (frmTrabajadores)
        JDesktopPane desktopPane = this.getDesktopPane();
        if (desktopPane == null) {
            JOptionPane.showMessageDialog(this, "No se pudo acceder al DesktopPane principal.");
            return;
        }
        // 2Ô∏è‚É£ Crear instancia del nuevo formulario hijo
        frmNuevoTrabajador nuevoTrabajador = null;

        // Verificamos si ya existe un frmNuevoTrabajador abierto
        for (JInternalFrame frame : desktopPane.getAllFrames()) {
            if (frame instanceof frmNuevoTrabajador) {
                nuevoTrabajador = (frmNuevoTrabajador) frame;
                break;
            }
        }

        if (nuevoTrabajador != null) {
            // Si ya existe, lo traemos al frente
            try {
                nuevoTrabajador.setIcon(false);
                nuevoTrabajador.setSelected(true);
                nuevoTrabajador.toFront();
                nuevoTrabajador.habilitarCampos();
                nuevoTrabajador.aplicarPermisosTrabajador(); // aplica permisos seg√∫n rol

            } catch (PropertyVetoException e) {
                e.printStackTrace();
            }
        } else {
            // Si no existe, creamos uno nuevo
            nuevoTrabajador = new frmNuevoTrabajador();
            desktopPane.add(nuevoTrabajador);
            nuevoTrabajador.setVisible(true);
            nuevoTrabajador.habilitarCampos(); // ‚úÖ Nuevo formulario = campos activos
            nuevoTrabajador.aplicarPermisosTrabajador(); // aplica permisos seg√∫n rol
            try {
                nuevoTrabajador.setSelected(true);
            } catch (PropertyVetoException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnNuevoTrabajadorActionPerformed

