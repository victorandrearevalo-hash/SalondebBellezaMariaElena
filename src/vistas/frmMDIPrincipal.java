    private void pasteMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pasteMenuItemActionPerformed
        // Verificar si el formulario ya está abierto
        frmCliente clienteForm = null;
        for (JInternalFrame frame : desktopPane.getAllFrames()) {
            if (frame instanceof frmCliente) {
                clienteForm = (frmCliente) frame;
                break;
            }
        }
        // Si ya existe, traerlo al frente
        if (clienteForm != null) {
            try {
                clienteForm.setSelected(true);
                clienteForm.toFront();
            } catch (PropertyVetoException e) {
                e.printStackTrace();
            }
        } else {
            // Si no existe, crear una nueva instancia
            clienteForm = new frmCliente();
            desktopPane.add(clienteForm);
            clienteForm.setVisible(true);
            
        }
    }//GEN-LAST:event_pasteMenuItemActionPerformed

